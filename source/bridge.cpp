            dup2(pipefd[1], STDOUT_FILENO);
            close(pipefd[1]);
            int devnull = open("/dev/null", O_WRONLY);
            if (devnull != -1) {
                dup2(devnull, STDERR_FILENO);
                close(devnull);
            }
            execlp("unzip", "unzip", "-p", pk3_path.c_str(), lump_name.c_str(), (char*)nullptr);
            _exit(EXIT_FAILURE);
